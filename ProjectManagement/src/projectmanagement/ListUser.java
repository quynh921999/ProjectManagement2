    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        if(txt_username.getText().equals("") || txt_pass.getText().equals("") || box_division.getSelectedItem().toString().equals("Select") || txt_fullname.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill full the information!");
            return;
        }
            
        String sql = "insert into login(username, password, Division, fullName, workAs) values(?, ?, ?, ?, ?)";
        try {
            login.dbc.ps = login.dbc.conn.prepareStatement(sql);
            login.dbc.ps.setString(1, txt_username.getText());
            login.dbc.ps.setString(2, txt_pass.getText());
            if(box_division.getSelectedItem().toString().equals("Developer") || box_division.getSelectedItem().toString().equals("Tester"))
                login.dbc.ps.setString(3,"Employee");
            else
                login.dbc.ps.setString(3, box_division.getSelectedItem().toString());
            login.dbc.ps.setString(4, txt_fullname.getText());
            login.dbc.ps.setString(5, box_division.getSelectedItem().toString());
            login.dbc.ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Create new user susseccfully!");
        } catch (SQLException ex) {
            Logger.getLogger(ListUser.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_createActionPerformed

