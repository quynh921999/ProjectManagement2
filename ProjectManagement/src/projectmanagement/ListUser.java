    public ArrayList<User> getListUser(){
        ArrayList<User> list = new ArrayList<>();
        String sql = "select * from login";
        try {
            login.dbc.st = login.dbc.conn.createStatement();
            login.dbc.rs = login.dbc.st.executeQuery(sql);
        } catch (SQLException ex) {
            Logger.getLogger(ListUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while(login.dbc.rs.next()){
                String name = login.dbc.rs.getString("fullName");
                String divi = login.dbc.rs.getString("workAs");
                String user = login.dbc.rs.getString("username");
                User newUser = new User(name, divi, user);
                list.add(newUser);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ListUser.class.getName()).log(Level.SEVERE, null, ex);
        }
        return list;
        
    }
    public void showListUser(){
        ArrayList<User> list = getListUser();
        Object[] rows = new Object[3];
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for(int i = 0; i < list.size(); i++){
            rows[0] = list.get(i).getName();
            rows[1] = list.get(i).getDivision();
            rows[2] = list.get(i).getUsername();
            model.addRow(rows);
        }
    }
    public void showListUserFilter(){
        ArrayList<User> list = getListUser();
        if(division.getSelectedItem().toString().equals("Select")){
            showListUser();
            return;
        } else {
            ArrayList<User> newList = new ArrayList<>();
            for (User user1 : list) {
                if(user1.getDivision().equals(division.getSelectedItem().toString())){
                    newList.add(user1);
                }
            }
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Object[] rows = new Object[3];
            for(int i = 0; i < newList.size(); i++){
                rows[0] = newList.get(i).getName();
                rows[1] = newList.get(i).getDivision();
                rows[2] = newList.get(i).getUsername();
                model.addRow(rows);
            }
        }
    }
